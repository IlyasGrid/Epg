                    <h5 class="pfp">L'Organisation de la Formation</h5>

                </div>

                <p>L'enseignement au titre de la formation initiale se déroule sur deux années de formation et comporte
                    2415 heures de cours (Y compris 480 heures de stage en entreprise). Les cours sont organisés en
                    général du lundi au vendredi, du début Octobre à la fin Juillet, permettant ainsi :</p>

                <ul>

                    <li><span>&#10022;</span> Acquisition de connaissances dans le cadre des travaux dirigés</li>

                    <li><span>&#10022;</span> Acquisition de savoir-faire pluridisciplinaires pendant la formation :
                        maîtrise des logiciels, jeux de simulation, activités de synthèse</li>

                    <li><span>&#10022;</span> Pédagogie en situation réelle : revue de presse, études et projets, stages
                    </li>

                    <li><span>&#10022;</span> Recours aux moyens offerts par la technologie : Audiovisuel,
                        micro-ordinateurs, multimédia, internet, rétroprojecteurs.</li>

                    <li><span>&#10022;</span> Quelques interventions ou conférences sont parfois programmées le mardi,
                        en fonction des disponibilités des intervenants.</li>

                    <li><span>&#10022;</span> Des stages en entreprises sont prévus à la fin de chaque année de
                        formation. La rédaction d'un projet tutoré et d'un mémoire faisant suite au stage est demandée,
                        ainsi qu'une soutenance devant un jury composé de professionnels.</li>

                </ul>

                <div class="share clr_Orange">

                    <h5 class="pfp">Débouchés</h5>

                </div>

                <p>Les perspectives professionnelles associées aux technologies de l'information sont assez bonnes. À
                    l'entrée en fonction, la rémunération mensuelle varie énormément selon le genre et la taille de
                    l'entreprise et, après cinq ans d'expérience, le salaire peut atteindre 10 000 DH.</p>

                <p>Les possibilités d'avancement sont variées et s'inscrivent dans le sillage de leurs goûts et de leurs
                    champs d'intérêt. Normalement, après quelques années d'expérience, ils peuvent accéder à des postes
                    d'analyste ou de chef de projet. D'autres préfèrent poursuivre dans le domaine de la programmation.
                </p>

