                            <h5 class="pfp">TSDI</h5>

                        </div>

                        <p>Les Techniciens Spécialisés en Développement Informatique ont pour fonction principale de
                            contribuer au développement de systèmes de gestion afin de répondre aux besoins des
                            entreprises. Ces personnes complètent les données de l'analyse, développent, modifient et
                            adaptent des applications en conformité avec la législation marocaine, les mettent à
                            l'essai, les documentent, les implantent et les intègrent, s'il y a lieu. <a
                                href="#ch">&#9886; Pré-inscription en ligne &#9887;</a></p>

                    </div>

                </div>

                <p>De plus, les Techniciens Spécialisés en Développement Informatique assurent l'entretien des logiciels
                    existants en y apportant des changements et jouent un rôle de personnes ressources auprès des
                    utilisateurs. Ils peuvent être aussi appelés à assurer la formation des utilisateurs.</p>

                <p>Les responsabilités confiées aux Techniciens Spécialisés en Développement Informatique peuvent varier
                    en nature et en nombre, car elles sont largement tributaires du type de service à l’intérieur duquel
                    elles sont exercées et du mode de gestion qu’emploie la direction. Toutefois, dans le champ des
                    responsabilités qui leur est propre, les Techniciens Spécialisés en Développement Informatique
                    bénéficient d’une certaine autonomie sur le plan technique quant à l’organisation de leur travail.
                    Il faut aussi mentionner que ces personnes travaillent aussi souvent en équipe multidisciplinaire.
                    De ce fait, ils sont en étroite relation avec le chef de projet, les spécialistes de l'architecture
                    et les gestionnaires de réseaux.</p>



